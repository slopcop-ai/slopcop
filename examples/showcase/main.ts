console.log("в•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ\n");

const client = new Anthropic();
const findings: z.infer<typeof Finding.schema>[] = [];

const messages: Anthropic.MessageParam[] = [
	{
		role: "user",
		content: userPrompt.render({ language: "TypeScript", diff }),
	},
];

let turn = 0;
while (true) {
	turn++;
	const response = await client.messages.create({
		model: "claude-sonnet-4-20250514",
		max_tokens: 4096,
		system: systemPrompt,
		tools: claudeTools,
		messages,
	});

	const toolResults: Anthropic.ToolResultBlockParam[] = [];

	for (const block of response.content) {
		if (block.type === "text") {
			console.log("Assistant:", block.text);
		} else if (block.type === "tool_use") {
			const tool = registry.all.find((t) => (t.name as string) === block.name);
			if (!tool) continue;

			// Validate input against the Zod schema вЂ” catches malformed LLM output
			const parsed = tool.inputSchema.parse(block.input);
			console.log(`  в†’ ${block.name}:`, JSON.stringify(parsed, null, 2));

			if (block.name === "report_finding") {
				findings.push(parsed as z.infer<typeof Finding.schema>);
			}

			toolResults.push({
				type: "tool_result",
				tool_use_id: block.id,
				content: JSON.stringify({ ok: true }),
			});
		}
	}

	if (response.stop_reason === "end_turn") break;

	messages.push({ role: "assistant", content: response.content });
	messages.push({ role: "user", content: toolResults });
}

// в”Ђв”Ђ Final report в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

console.log("\nв”Ђв”Ђ Findings в”Ђв”Ђ\n");
for (const f of findings) {
	console.log(`  [${f.severity}] ${f.category} вЂ” ${f.title}`);
	console.log(`    ${f.filePath}:${f.lineRange.start}-${f.lineRange.end}`);
	console.log(`    ${f.explanation}\n`);
}
